export function getHexagram(number: number): Hexagram | undefined {
  return hexagrams[number];
}

export function getHexagramTranslation(number: number, lang: string): Hexagram['translations'][string] | undefined {
  const hex = hexagrams[number];
  if (!hex) return undefined;
  return hex.translations[lang] || hex.translations.en;
}

/**
 * Get hexagram translation by language and translation source.
 * Falls back to source-agnostic translation if source-specific not available,
 * then to English, then to any available translation.
 *
 * @param number Hexagram number (1-64)
 * @param lang Language code (e.g., 'en', 'es', 'zh')
 * @param source Translation source ('wilhelm' or 'legge')
 * @returns Translation object or undefined
 */
export function getHexagramTranslationBySource(
  number: number,
  lang: string,
  source: TranslationSource = 'wilhelm'
): Hexagram['translations'][string] | undefined {
  const hex = hexagrams[number];
  if (!hex) return undefined;

  // Try source-specific key first (e.g., 'en-legge')
  const sourceSpecificKey = `${lang}-${source}`;
  if (sourceSpecificKey in hex.translations) {
    return hex.translations[sourceSpecificKey];
  }

  // Fall back to language without source (default to Wilhelm)
  if (lang in hex.translations) {
    return hex.translations[lang];
  }

  // Fall back to English without source
  if ('en' in hex.translations) {
    return hex.translations.en;
  }

  // Last resort: return first available translation
  const keys = Object.keys(hex.translations);
  if (keys.length > 0) {
    return hex.translations[keys[0]];
  }

  return undefined;
}
